function convertMinutesToSeconds(minutes) {
  return 60 * minutes;
}

function greet(name) {
  return 'Hey, ' + name;
}

function getArea(width, height) {
  return width * height;
}

function getFirstName(person) {
  person.firstName;
  person.lastName;
  return person.firstName;
}

function getLastElement(array) {
  var last = array.length - 1;
  var lastElement = array[last];
  return lastElement;
}
