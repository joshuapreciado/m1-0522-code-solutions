// Return result
// ~ Returned the char method of the word object with
// zero arguements, along with the toUpperCase method of
// the word object with no arguement being concatinated
// with the slice method of the word object with one arguement
// being the number one, then the lowercase method of the
// word object is being called with no arguement, and the result
// gets returned - Completed

function capitalize(word) {
  return word.charAt().toUpperCase() + word.slice(1).toLowerCase();
}
